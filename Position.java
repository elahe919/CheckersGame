public class Position {
    private int row;
    private int column;
    //qabeliat dastresi va entekhab mokhtasat har mohre
    public Position(int row, int col) {
        this.row = row;
        this.column = col;
    }

    public int getRow() {

        return row;
    }

    public int getColumn() {

        return column;
    }
}
